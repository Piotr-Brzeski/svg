template<class Model>
std::pair<int, int> find(Model const& model, int value) {
  std::size_t x = 0;
  std::size_t y = 0;
  for(auto& line : model) {
    auto found = std::find(line.begin(), line.end(), value);
    if(found != line.end()) {
      x = found - line.begin();
      return {x, y};
    }
    ++y;
  }
  throw std::runtime_error("Value not found");
}

std::pair<int, int> operator+(std::pair<int, int> const& a, std::pair<int, int> const& b) {
  return {a.first + b.first, a.second - b.second};
}


